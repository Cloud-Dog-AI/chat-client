set -euo pipefail

MODE="${1:-${CHAT_CLIENT_MODE:-api}}"
ENV_FILE="${CHAT_CLIENT_ENV_FILE:-/app/env-docker-defaults}"
API_HOST="${CLOUD_DOG__API_SERVER__HOST:-${CHAT_CLIENT_API_HOST:-0.0.0.0}}"
API_PORT="${CLOUD_DOG__API_SERVER__PORT:-${CHAT_CLIENT_API_PORT:-0}}"
WEB_PORT="${CLOUD_DOG__WEB_SERVER__PORT:-${CHAT_CLIENT_WEB_PORT:-0}}"
MCP_PORT="${CLOUD_DOG__MCP_SERVER__PORT:-${CHAT_CLIENT_MCP_PORT:-0}}"
A2A_PORT="${CLOUD_DOG__A2A_SERVER__PORT:-${CHAT_CLIENT_A2A_PORT:-0}}"
APP_USER="$(id -un)"
APP_HOME="${HOME:-/home/${APP_USER}}"

print_banner() {
  echo "============================================================"
  echo "cloud-dog-chat-client container"
  echo "============================================================"
  echo "Mode: ${MODE}"
  echo "Env file: ${ENV_FILE}"
  echo "API bind: ${API_HOST}:${API_PORT}"
  echo "Web bind: 0.0.0.0:${WEB_PORT}"
  echo "MCP bind: 0.0.0.0:${MCP_PORT}"
  echo "A2A bind: 0.0.0.0:${A2A_PORT}"
  echo "Run user: ${APP_USER}"
  echo "============================================================"
}

setup_shell_runtime() {
  local runtime_env_file="/app/chat-client-runtime-env.sh"
  {
    echo "#!/usr/bin/env bash"
    echo "# generated by docker-entrypoint.chat-client.sh"
    while IFS='=' read -r key value; do
      printf 'export %s=%q\n' "$key" "$value"
    done < <(env | grep -E '^(CLOUD_DOG__|CHAT_CLIENT_)')
  } > "${runtime_env_file}"
  chmod 0644 "${runtime_env_file}"

  local bash_profile="${APP_HOME}/.bash_profile"
  local marker="# cloud-dog-chat-client:auto-chat"
  if [[ ! -f "${bash_profile}" ]] || ! grep -q "${marker}" "${bash_profile}"; then
    cat >> "${bash_profile}" <<'PROFILE_EOF'
# cloud-dog-chat-client:auto-chat
if [[ -f /app/chat-client-runtime-env.sh ]]; then
  source /app/chat-client-runtime-env.sh
fi
if [[ -n "${SSH_TTY:-}" ]] && command -v cloud-dog-chat >/dev/null 2>&1; then
  export CLOUD_DOG__APP__ENV_FILE="${CHAT_CLIENT_ENV_FILE:-/app/env-docker-defaults}"
  export CLOUD_DOG__APP__PROJECT_ROOT="${CLOUD_DOG__APP__PROJECT_ROOT:-/app}"
  export CLOUD_DOG__APP__LOGFOLDER="${CHAT_CLIENT_LOGFOLDER:-/tmp/chat-client-logs}"
  mkdir -p "${CLOUD_DOG__APP__LOGFOLDER}"
  printf 'Press ? for help\n'
  exec cloud-dog-chat chat --no-rich
fi
PROFILE_EOF
  fi
}

run_migration_preflight() {
  echo "Running database migration preflight..."
python3 - <<'PY'
import os
from pathlib import Path
import sys

sys.path.insert(0, str(Path("/app/src")))

from cloud_dog_chat_client.config import ConfigManager
from cloud_dog_chat_client.database.runtime import ChatDatabaseRuntime

cfg = ConfigManager(env_file=os.environ.get("CHAT_CLIENT_ENV_FILE") or "/app/env-docker-defaults")
runtime = ChatDatabaseRuntime(cfg)
runtime.dispose()
print("Database migration preflight complete")
PY
}

print_banner
setup_shell_runtime

export CLOUD_DOG__APP__PROJECT_ROOT="${CLOUD_DOG__APP__PROJECT_ROOT:-/app}"
export CLOUD_DOG__APP__ENV_FILE="${ENV_FILE}"
export CLOUD_DOG__API_SERVER__HOST="${API_HOST}"
export CLOUD_DOG__API_SERVER__PORT="${API_PORT}"
export CLOUD_DOG__API_SERVER__BASE_URL="${CLOUD_DOG__API_SERVER__BASE_URL:-http://127.0.0.1:${API_PORT}}"
export CLOUD_DOG__API_SERVER__HOST="${CLOUD_DOG__API_SERVER__HOST:-${API_HOST}}"
export CLOUD_DOG__API_SERVER__PORT="${CLOUD_DOG__API_SERVER__PORT:-${API_PORT}}"
export CLOUD_DOG__WEB_SERVER__HOST="${CLOUD_DOG__WEB_SERVER__HOST:-0.0.0.0}"
export CLOUD_DOG__WEB_SERVER__PORT="${CLOUD_DOG__WEB_SERVER__PORT:-${WEB_PORT}}"
export CLOUD_DOG__MCP_SERVER__HOST="${CLOUD_DOG__MCP_SERVER__HOST:-0.0.0.0}"
export CLOUD_DOG__MCP_SERVER__PORT="${CLOUD_DOG__MCP_SERVER__PORT:-${MCP_PORT}}"
export CLOUD_DOG__A2A_SERVER__HOST="${CLOUD_DOG__A2A_SERVER__HOST:-0.0.0.0}"
export CLOUD_DOG__A2A_SERVER__PORT="${CLOUD_DOG__A2A_SERVER__PORT:-${A2A_PORT}}"

case "${MODE}" in
  all)
    run_migration_preflight
    pids=()
    terminate_children() {
      for pid in "${pids[@]:-}"; do
        kill "${pid}" 2>/dev/null || true
      done
      wait || true
    }
    trap terminate_children EXIT INT TERM

    python3 /app/start_api_server.py &
    pids+=("$!")
    python3 /app/start_web_server.py &
    pids+=("$!")
    python3 /app/start_mcp_server.py &
    pids+=("$!")
    python3 /app/start_a2a_server.py &
    pids+=("$!")
    wait -n "${pids[@]}"
    exit $?
    ;;
  api|server|test-server)
    run_migration_preflight
    exec python3 /app/start_api_server.py
    ;;
  web)
    exec python3 /app/start_web_server.py
    ;;
  mcp)
    run_migration_preflight
    exec python3 /app/start_mcp_server.py
    ;;
  a2a)
    run_migration_preflight
    exec python3 /app/start_a2a_server.py
    ;;
  chat)
    exec cloud-dog-chat chat --no-rich
    ;;
  status)
    exec /app/server_control.sh --env "${ENV_FILE}" status all
    ;;
  shell|bash)
    exec /bin/bash
    ;;
  *)
    exec "$@"
    ;;
esac
